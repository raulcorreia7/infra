#!/usr/bin/env bash
set -euo pipefail

SCRIPT_DIR="$(cd -- "$(dirname -- "${BASH_SOURCE[0]}")" && pwd)"
ROOT_DIR="$(cd -- "${SCRIPT_DIR}/.." && pwd)"
# shellcheck source=bin/lib/common.sh
. "${ROOT_DIR}/bin/lib/common.sh"

HOST_NAME="${1:-}"
HOST_DIR=""
ENV_FILE=""
declare -a ENABLED_STACKS=()

usage() {
	cat <<'EOF'
Usage: bin/setup.sh <host>

Prepare one host for running enabled Docker Compose stacks.

Options:
  -h, --help Show this help.
EOF
}

update_host_env_var() {
	local key="$1"
	local value="$2"
	local temp_file=""
	local line=""
	local found=false

	temp_file="$(mktemp)"

	while IFS= read -r line || [[ -n "$line" ]]; do
		if [[ "$line" == "${key}="* ]]; then
			printf '%s=%s\n' "$key" "$value" >>"$temp_file"
			found=true
			continue
		fi

		printf '%s\n' "$line" >>"$temp_file"
	done <"$ENV_FILE"

	if [[ "$found" != true ]]; then
		printf '%s=%s\n' "$key" "$value" >>"$temp_file"
	fi

	mv "$temp_file" "$ENV_FILE"
}

ensure_generated_host_secret() {
	local key="$1"
	local current_value="${!key:-}"
	local generated_value=""

	if [[ -n "$current_value" && "$current_value" != change-me-* ]]; then
		return
	fi

	generated_value="$(openssl rand -hex 32)"
	printf -v "$key" '%s' "$generated_value"
	export "$key=$generated_value"
	update_host_env_var "$key" "$generated_value"
	printf 'generated %s in %s\n' "$key" "${ENV_FILE#"${ROOT_DIR}/"}"
}

ensure_generated_host_secrets() {
	local key=""
	local secrets=(
		HEADPLANE_COOKIE_SECRET
		KOMODO_DB_PASSWORD
		KOMODO_PASSKEY
		KOMODO_WEBHOOK_SECRET
		KOMODO_JWT_SECRET
		KOMODO_INIT_ADMIN_PASSWORD
		FORGEJO_SECRET_KEY
		FORGEJO_INTERNAL_TOKEN
		FORGEJO_JWT_SECRET
		FORGEJO_LFS_JWT_SECRET
	)

	for key in "${secrets[@]}"; do
		if host_uses_env_key "$key"; then
			ensure_generated_host_secret "$key"
		fi
	done
}

copy_example_file_once() {
	local example_file="$1"
	local target_file=""

	target_file="$(target_path_for_example "$example_file")"
	[[ -e "$target_file" ]] && return

	cp "$example_file" "$target_file"
	printf 'created %s\n' "${target_file#"${ROOT_DIR}/"}"
}

sync_stack_compose_env() {
	local stack_directory="$1"
	local keys_file="${stack_directory}/stack.env.keys"
	local target_file="${stack_directory}/.env"
	local key=""
	local value=""

	[[ -f "$keys_file" ]] || fail "missing ${keys_file}; define the stack env allowlist"

	: >"$target_file"
	printf '# generated by ./bin/setup.sh %s\n' "$HOST_NAME" >>"$target_file"

	while IFS= read -r key || [[ -n "$key" ]]; do
		key="${key%%#*}"
		key="$(trim "$key")"
		[[ -n "$key" ]] || continue

		value="${!key:-}"
		[[ -n "$value" ]] || fail "missing ${key} for ${stack_directory#"${ROOT_DIR}/"}"
		printf '%s=%s\n' "$key" "$value" >>"$target_file"
	done <"$keys_file"

	printf 'synced %s\n' "${target_file#"${ROOT_DIR}/"}"
}

render_template_file_once() {
	local template_file="$1"
	local target_file=""

	target_file="$(target_path_for_template "$template_file")"
	[[ -e "$target_file" ]] && return

	render_template_to_file "$template_file" "$target_file"
	printf 'rendered %s\n' "${target_file#"${ROOT_DIR}/"}"
}

ensure_edge_network() {
	[[ -n "${EDGE_NETWORK:-}" ]] || fail "EDGE_NETWORK must be set in ${ENV_FILE}"

	if docker network inspect "$EDGE_NETWORK" >/dev/null 2>&1; then
		printf 'network already present: %s\n' "$EDGE_NETWORK"
		return
	fi

	docker network create "$EDGE_NETWORK" >/dev/null
	printf 'created external network: %s\n' "$EDGE_NETWORK"
}

ensure_stack_directories() {
	local stack_name="$1"
	local stack_directory="$2"

	case "$stack_name" in
	headscale_vpn)
		mkdir -p "$stack_directory/data/headscale" "$stack_directory/data/headplane"
		printf 'ensured %s\n' "${stack_directory#"${ROOT_DIR}/"}/data/headscale"
		printf 'ensured %s\n' "${stack_directory#"${ROOT_DIR}/"}/data/headplane"
		;;
	forgejo)
		mkdir -p "$stack_directory/data/forgejo" "$stack_directory/config"
		chown 1000:1000 "$stack_directory/data/forgejo" >/dev/null 2>&1 || true
		printf 'ensured %s\n' "${stack_directory#"${ROOT_DIR}/"}/data/forgejo"
		;;
	komodo)
		mkdir -p "$stack_directory/data/postgres" "$stack_directory/data/ferretdb" "$stack_directory/data/backups" "$stack_directory/data/periphery"
		printf 'ensured %s\n' "${stack_directory#"${ROOT_DIR}/"}/data/postgres"
		printf 'ensured %s\n' "${stack_directory#"${ROOT_DIR}/"}/data/ferretdb"
		printf 'ensured %s\n' "${stack_directory#"${ROOT_DIR}/"}/data/backups"
		printf 'ensured %s\n' "${stack_directory#"${ROOT_DIR}/"}/data/periphery"
		;;
	reverse_proxy)
		mkdir -p "$stack_directory/data/caddy" "$stack_directory/data/config"
		printf 'ensured %s\n' "${stack_directory#"${ROOT_DIR}/"}/data/caddy"
		printf 'ensured %s\n' "${stack_directory#"${ROOT_DIR}/"}/data/config"
		;;
	*)
		mkdir -p "$stack_directory/data"
		printf 'ensured %s\n' "${stack_directory#"${ROOT_DIR}/"}/data"
		;;
	esac
}

prepare_stack() {
	local stack_name="$1"
	local stack_directory=""
	local template_file=""
	local example_file=""

	stack_directory="$(stack_dir "$stack_name")"

	[[ -d "$stack_directory" ]] || fail "missing stack directory: ${stack_directory}"
	ensure_stack_directories "$stack_name" "$stack_directory"
	sync_stack_compose_env "$stack_directory"

	while IFS= read -r template_file; do
		render_template_file_once "$template_file"
	done < <(find "$stack_directory" -path "$stack_directory/data" -prune -o -type f \( -name '*.template' -o -name '*.template.*' \) -print | sort)

	while IFS= read -r example_file; do
		copy_example_file_once "$example_file"
	done < <(find "$stack_directory" -path "$stack_directory/data" -prune -o -type f \( -name '*.example' -o -name '*.example.*' \) -print | sort)
}

main() {
	if is_help_flag "$HOST_NAME"; then
		usage
		return
	fi

	if [[ -z "$HOST_NAME" ]]; then
		usage >&2
		exit 1
	fi

	set_host_paths
	require_local_env_file
	load_host_env
	load_enabled_stacks
	ensure_generated_host_secrets

	if [[ "${#ENABLED_STACKS[@]}" -eq 0 ]]; then
		printf 'no enabled stacks for host %s\n' "$HOST_NAME"
		return
	fi

	require_command docker
	require_command envsubst
	require_command openssl
	require_docker_compose
	ensure_edge_network

	local stack_name=""
	for stack_name in "${ENABLED_STACKS[@]}"; do
		prepare_stack "$stack_name"
	done
}

main "$@"
